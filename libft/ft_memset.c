#include "libft.h"

void	*ft_memset(void *s, int value, size_t n)
{
	size_t			i;
	unsigned char	*p;

	p = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		p[i] = (unsigned char)value;
		++i;
	}
	return (s);
}
